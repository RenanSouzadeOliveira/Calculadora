package calc;

public class Calculo {
	
	public double soma(double i, double n){
		double total = 0;
		total = i + n;
		return total;
	}
	
	public double mult(double i, double n){
		double total = 0;
		total = i * n;
		return total;
	}
	
	public double div(double i, double n){
		double total = 0;
		total = i / n;
		return total;
	}
	
	public double sub(double i, double n){
		double total = 0;
		total = i - n;
		return total;
	}
}
